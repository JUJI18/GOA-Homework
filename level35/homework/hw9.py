#   9) შექმენით ფუნქცია რომელიც მომხმარებელს შეეკითხება ასაკს თუ მისი ასაკი მეტია 18 ზე უთხრას რომ სრულწლოვანია თუარადა არარის


def user_age():
    age= int(input("დაწერე შენი ასაკი:"))
    if age > 17:
        print("შენ ხარ სრულწლოვანი")
    elif age < 18:
        print("შენ ხარ არასრულწლოვანი")


user_age()