num1 = int(input("დაწერე რიცხვი:"))



def user_num(num):
    if num >0:
        print("ეს რიცხვი დადებითია")
    elif num <0:
        print("ეს რიცხვი უარყოფითია")
    elif num == 0:
        print("შენ დაწერე ნულიანი!")

user_num(num1)